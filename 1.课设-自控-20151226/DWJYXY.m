%单位阶跃响应
Kg=7;
Km=9;
num=[5400*Kg*Km];
den=[1,11+0.62*Km,10+6.2*Km+5400*Kg*Km];
sys=tf(num,den);
figure(1);
step(sys);
grid;
%根轨迹图取Kg=6,Km从0变到无穷大，等效开环传递函数Km*(0.62s+6.2+5400*Kg))/(s^2+11*s+10)
G=tf([0.62,6.2+5400*Kg],[1,11,10]);
figure(2);
rlocus(G);
grid;
%伯德图(开环传递函数与闭环传递函数)
GK=tf([5400*Kg*Km],[1,11+0.62*Km,10+6.2*Km]);
figure(3);
bode(GK);
title('开环对数幅频相频曲线');
grid;
figure(4);
bode(sys);
title('闭环对数幅频相频曲线');
grid;
